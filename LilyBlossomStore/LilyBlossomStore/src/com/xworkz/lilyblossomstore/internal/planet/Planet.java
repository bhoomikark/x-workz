package com.xworkz.lilyblossomstore.internal.planet;

public class Planet {
    public Planet() {
        System.out.println("A new planet is formed...");
    }

    public void rotate() {
        System.out.println("Planet is rotating...");
    }

    public void revolve() {
        System.out.println("Planet is revolving around the sun...");
    }

    public void hasAtmosphere() {
        System.out.println("Some planets have atmospheres...");
    }

    public void containsWater() {
        System.out.println("Some planets have water...");
    }

    public void supportsLife() {
        System.out.println("Very few planets support life...");
    }
}
