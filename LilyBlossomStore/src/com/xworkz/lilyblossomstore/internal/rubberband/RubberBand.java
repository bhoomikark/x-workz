package com.xworkz.lilyblossomstore.internal.rubberband;

public class RubberBand {
    public RubberBand() {
        System.out.println("Running RubberBand Constructor in RubberBand class");
    }

    public void stretch() {
        System.out.println("Stretching the rubber band.");
    }

    public void hold() {
        System.out.println("Rubber band is holding items together.");
    }

    public void breakIfOverstretched() {
        System.out.println("Rubber band may break if overstretched.");
    }

    public void shrink() {
        System.out.println("Rubber band is shrinking back to its original size.");
    }

    public void twist() {
        System.out.println("Twisting the rubber band.");
    }
}
