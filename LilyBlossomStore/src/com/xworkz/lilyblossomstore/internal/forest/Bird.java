package com.xworkz.lilyblossomstore.internal.forest;

public class Bird {
    public Bird() {
        System.out.println("A new bird is born...");
    }

    public void fly() {
        System.out.println("Bird is flying...");
    }

    public void sing() {
        System.out.println("Bird is singing...");
    }

    public void eat() {
        System.out.println("Bird is eating seeds and insects...");
    }

    public void buildNest() {
        System.out.println("Bird is building a nest...");
    }

    public void migrate() {
        System.out.println("Bird is migrating to another region...");
    }
}
