package com.xworkz.lilyblossomstore.internal.remotecar;

public class RemoteCar {
    public RemoteCar() {
        System.out.println("Running RemoteCar Constructor in RemoteCar class");
    }

    public void start() {
        System.out.println("Starting the remote-controlled car.");
    }

    public void stop() {
        System.out.println("Stopping the remote-controlled car.");
    }

    public void accelerate() {
        System.out.println("Accelerating the remote-controlled car.");
    }

    public void turnLeft() {
        System.out.println("Turning the remote-controlled car left.");
    }

    public void turnRight() {
        System.out.println("Turning the remote-controlled car right.");
    }
}
